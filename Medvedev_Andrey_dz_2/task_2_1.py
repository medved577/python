#1. Выяснить тип результата выражений:
#15 * 3
#15 / 3
#15 // 2
#15 ** 2

type_1 = str(type (15 * 3))
print ("Тип результата выражения '15 * 3' :"  + type_1)

type_2 = str(type (15 / 3))
print ("Тип результата выражения '15 / 3' :"  + type_2)

type_3 = str(type (15 // 2))
print ("Тип результата выражения '15 // 3' :"  + type_3)

type_4 = str(type (15 ** 2))
print ("Тип результата выражения '15 ** 3' :"  + type_4)